package Entidades;

/**
 *
 * @author Panda
 */
public class CeCos 
{
    String sociedad = "";
    String claseCoste = "";
    String descripcion = "";
    String centroCos = "";
    String denominacion = "";

    public String getSociedad() {
        return sociedad;
    }

    public void setSociedad(String sociedad) {
        this.sociedad = sociedad;
    }

    public String getClaseCoste() {
        return claseCoste;
    }

    public void setClaseCoste(String claseCoste) {
        this.claseCoste = claseCoste;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getCentroCos() {
        return centroCos;
    }

    public void setCentroCos(String centroCos) {
        this.centroCos = centroCos;
    }

    public String getDenominacion() {
        return denominacion;
    }

    public void setDenominacion(String denominacion) {
        this.denominacion = denominacion;
    }
    
    
}

package Entidades;

public class clase_informe {

    String clase_informe;
    String texto_breve;
    String perfil_catalogo;
    String texto_esquema_informe;

    public String getClase_informe() {
        return clase_informe;
    }

    public void setClase_informe(String clase_informe) {
        this.clase_informe = clase_informe;
    }

    public String getTexto_breve() {
        return texto_breve;
    }

    public void setTexto_breve(String texto_breve) {
        this.texto_breve = texto_breve;
    }

    public String getPerfil_catalogo() {
        return perfil_catalogo;
    }

    public void setPerfil_catalogo(String perfil_catalogo) {
        this.perfil_catalogo = perfil_catalogo;
    }

    public String getTexto_esquema_informe() {
        return texto_esquema_informe;
    }

    public void setTexto_esquema_informe(String texto_esquema_informe) {
        this.texto_esquema_informe = texto_esquema_informe;
    }

}

public class clase_orden {
    int id_co;
    String clase_orden;
    String tipo_orden;
    String descripcion;

    public int getId_co() {
        return id_co;
    }

    public void setId_co(int id_co) {
        this.id_co = id_co;
    }

    public String getClase_orden() {
        return clase_orden;
    }

    public void setClase_orden(String clase_orden) {
        this.clase_orden = clase_orden;
    }

    public String getTipo_orden() {
        return tipo_orden;
    }

    public void setTipo_orden(String tipo_orden) {
        this.tipo_orden = tipo_orden;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
}
